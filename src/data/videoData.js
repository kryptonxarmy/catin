// Fungsi helper untuk mengambil video berdasarkan kategori
export const getVideosByCategory = (category) => {
  if (category === "Semua") return videoData;
  return videoData.filter((video) => video.category === category);
};

// Fungsi helper untuk mengambil video featured
export const getFeaturedVideos = () => {
  return videoData.filter((video) => video.featured);
};

// Fungsi helper untuk mengambil video berdasarkan slug
export const getVideoBySlug = (slug) => {
  return videoData.find((video) => video.slug === slug);
};

// Fungsi helper untuk mengambil video terkait (berdasarkan kategori, kecuali video saat ini)
export const getRelatedVideos = (currentSlug, category, limit = 3) => {
  return videoData.filter((video) => video.slug !== currentSlug && video.category === category).slice(0, limit);
};

// Fungsi helper untuk mendapatkan warna kategori (sama dengan artikel)
export const getCategoryColor = (category) => {
  const colors = {
    "Teman Finansial": "bg-green-100 text-green-700 border-green-200",
    "Teman Nikah": "bg-pink-100 text-pink-700 border-pink-200",
    "Teman Belajar": "bg-blue-100 text-blue-700 border-blue-200",
    "Teman Parenting": "bg-yellow-100 text-yellow-700 border-yellow-200",

    // "Panduan Catin": "bg-indigo-100 text-indigo-700 border-indigo-200",
  };
  return colors[category] || "bg-gray-100 text-gray-700 border-gray-200";
};

